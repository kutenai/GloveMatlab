function myglobalfimath(FractionalLength)

% Set global fimath properties
Fm = fimath(...
'ProductMode', 'SpecifyPrecision', ...
'ProductWordLength', 32, ...
'ProductFractionLength', FractionalLength, ...
'SumMode', 'SpecifyPrecision', ...
'SumWordLength', 32, ...
'SumFractionLength', FractionalLength, ...
'RoundMode', 'Floor', ...
'OverflowMode', 'Wrap', ...
'CastBeforeSum', true);

globalfimath(Fm);